package Presentation;

/**
 * Enumeration representing the menu options for managing products.
 * This enum provides options for actions related to product management,
 * such as creating a new product, removing an existing product, and going back to the previous menu.
 */
public enum MenuOptionsManageProducts {
    /**
     * Option to create a new product.
     * This action initiates the process of adding a new product to the system.
     */
    CREATE_PRODUCT,

    /**
     * Option to remove an existing product.
     * This action initiates the process of deleting a product from the system.
     */
    REMOVE_PRODUCT,

    /**
     * Option to go back to the previous menu.
     * This action cancels the current operation and returns to the previous menu.
     */
    BACK,
}
