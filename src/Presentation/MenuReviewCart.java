package Presentation;

/**
 * Enumeration representing the menu options for reviewing products and managing the shopping cart.
 * This enum provides options for actions such as reading reviews, reviewing a product, and adding a product to the cart.
 */
public enum MenuReviewCart {
    /**
     * Option to read reviews of products.
     * This action allows the user to view existing reviews of a product.
     */
    READ_REVIEWS,

    /**
     * Option to review a product.
     * This action allows the user to write and submit a review for a product.
     */
    REVIEW_PRODCUCT,

    /**
     * Option to add a product to the shopping cart.
     * This action allows the user to add a selected product to their cart.
     */
    ADD_TO_CART,
}
