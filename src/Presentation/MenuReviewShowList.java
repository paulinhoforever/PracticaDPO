package Presentation;

/**
 * Enumeration representing the menu options for reviewing products.
 * This enum provides options for actions such as reading reviews and reviewing a product.
 */
public enum MenuReviewShowList {
    /**
     * Option to read reviews of products.
     * This action allows the user to view existing reviews for a product.
     */
    READ_REVIEWS,

    /**
     * Option to review a product.
     * This action allows the user to write and submit a review for a product.
     */
    REVIEW_PRODUCT,
}
